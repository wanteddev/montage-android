package com.wanted.android.wanted.design.navigations.tab


/**
 * object WantedTabContract
 *
 * 탭 컴포넌트에서 사용하는 설정 값을 정의하는 객체입니다.
 */
object WantedTabContract {

    /**
     * enum class TabSize
     *
     * 탭 텍스트의 크기를 정의하는 enum 클래스입니다.
     * Small, Medium, Large 세 가지 크기가 존재합니다.
     */
    enum class TabSize {
        Small,
        Medium,
        Large
    }
}